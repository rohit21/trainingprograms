package fileios;

import java.sql.Time;
import java.sql.Date;

public class Student 
{
	int id;
	Date date;
	Time time;
	boolean flag;
	public Student(int id,String date,String time,String flag)
	{
		this.id=id;
	   	this.time=Time.valueOf(time);
		this.date=Date.valueOf(date);
		this.flag=Boolean.parseBoolean(flag);
	}
	public Time getTime() {
		return time;
	}
	public void setTime(Time t) {
		this.time = t;
	}
	public void setId(int id) {
	    this.id= id;
	}
	public int getId() {
		return this.id;
	}
	public void setDate(Date d) {
	    this.date = d;
	}
	public Date getDate() {
	    return this.date;
	}
}

